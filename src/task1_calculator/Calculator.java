    private void EqualButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualButtonActionPerformed
       //Getting Final Product
       
       
       Entry2 = Double.parseDouble(ResultsTextField.getText());
       
       if(SumSign =="+")
       {   
       sum =  Entry1 +Entry2;
       ResultsTextField.setText(String.valueOf(sum));
           
       } else  if(SumSign =="-")
       {
       sum = Entry1 - Entry2;
       ResultsTextField.setText(String.valueOf(sum));
       }else  if(SumSign =="*")
       {
       sum = Entry1 * Entry2;
       ResultsTextField.setText(String.valueOf(sum));
       }else if(SumSign =="/")
       {
       sum = Entry1 / Entry2;
       ResultsTextField.setText(String.valueOf(sum));
       }
       
       
           }//GEN-LAST:event_EqualButtonActionPerformed

